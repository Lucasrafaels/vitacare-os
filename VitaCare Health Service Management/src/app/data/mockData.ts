// Helper para buscar dados relacionados
export function getUserById(id: string): User | undefined {
  return mockUsers.find(u => u.id === id);
}

export function getUnitById(id: string): HealthUnit | undefined {
  return mockHealthUnits.find(u => u.id === id);
}

export function getServiceOrdersWithRelations(): ServiceOrder[] {
  return mockServiceOrders.map(os => ({
    ...os,
    unit: getUnitById(os.unitId),
    assignedUser: os.assignedTo ? getUserById(os.assignedTo) : undefined,
    createdByUser: getUserById(os.createdBy),
  }));
}
